package modal;

public class Customer_complaint {
	
	private String complaint_name,complaint_type,complaint_desc,date,complaint_image,status;

	public String getComplaint_name() {
		return complaint_name;
	}

	public void setComplaint_name(String complaint_name) {
		this.complaint_name = complaint_name;
	}

	public String getComplaint_type() {
		return complaint_type;
	}

	public void setComplaint_type(String complaint_type) {
		this.complaint_type = complaint_type;
	}

	public String getComplaint_desc() {
		return complaint_desc;
	}

	public void setComplaint_desc(String complaint_desc) {
		this.complaint_desc = complaint_desc;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getComplaint_image() {
		return complaint_image;
	}

	public void setComplaint_image(String complaint_image) {
		this.complaint_image = complaint_image;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
	
	

}
